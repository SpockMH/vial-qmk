// セグメント定義
#define SEG_A (1 << 0)
#define SEG_B (1 << 1)
#define SEG_C (1 << 2)
#define SEG_D (1 << 3)
#define SEG_E (1 << 4)
#define SEG_F (1 << 5)
#define SEG_G (1 << 6)

static uint8_t get_seg_mask(char c) {
    switch (c) {
        case '0': return SEG_A | SEG_B | SEG_C | SEG_D | SEG_E | SEG_F;
        case '1': return SEG_B | SEG_C;
        case '2': return SEG_A | SEG_B | SEG_G | SEG_E | SEG_D;
        case '3': return SEG_A | SEG_B | SEG_G | SEG_C | SEG_D;
        case '4': return SEG_F | SEG_G | SEG_B | SEG_C;
        case '5': return SEG_A | SEG_F | SEG_G | SEG_C | SEG_D;
        case '6': return SEG_A | SEG_F | SEG_E | SEG_D | SEG_C | SEG_G;
        case '7': return SEG_A | SEG_B | SEG_C;
        case '8': return SEG_A | SEG_B | SEG_C | SEG_D | SEG_E | SEG_F | SEG_G;
        case '9': return SEG_A | SEG_B | SEG_C | SEG_D | SEG_F | SEG_G;
        case 'A':
        case 'a': return SEG_A | SEG_B | SEG_C | SEG_E | SEG_F | SEG_G;
        case 'B':
        case 'b': return SEG_F | SEG_E | SEG_G | SEG_C | SEG_D;
        case 'C':
        case 'c': return SEG_A | SEG_F | SEG_E | SEG_D;
        case 'D':
        case 'd': return SEG_B | SEG_G | SEG_E | SEG_D | SEG_C;
        case 'E':
        case 'e': return SEG_A | SEG_F | SEG_G | SEG_E | SEG_D;
        case 'F':
        case 'f': return SEG_A | SEG_F | SEG_G | SEG_E;
        case '-': return SEG_G;
        default:  return 0x00;
    }
}

void oled_write_7seg_char(uint8_t x, uint8_t y, char c) {
    uint8_t mask = get_seg_mask(c);
    char buffer[OLED_SEG7_BYTES];
    memset(buffer, 0, sizeof(buffer));

    // マスクに従って各セグメントデータをOR合成
    for (uint8_t i = 0; i < 7; i++) {
        if (mask & (1 << i)) {
            const unsigned char* seg_ptr = (const unsigned char*)pgm_read_ptr(&seg7_allArray[i]);
            for (uint16_t b = 0; b < OLED_SEG7_BYTES; b++) {
                buffer[b] |= pgm_read_byte(&seg_ptr[b]);
            }
        }
    }

    // OLED画面の指定位置へ書き込み
    for (uint8_t page = 0; page < 4; page++) {
        oled_set_cursor(x / 6, (y / 8) + page);
        oled_write_raw(&buffer[page * OLED_SEG7_WIDTH], OLED_SEG7_WIDTH);
    }
}

void oled_write_7seg_num(uint8_t x, uint8_t y, uint8_t num) {
    if (num > 9) {
        num = 9;
    }
    oled_write_7seg_char(x, y, '0' + num);
}