static const char *itoc(int32_t number, uint8_t width) {
    static char str[11]; // 最大10桁 + null終端分の余裕
    uint8_t i = 0;
    if (width > 10) width = 10;

    if (number < 0) number = 0; // 念のため負値をガード

    do {
        str[i++] = number % 10 + '0';
        number /= 10;
    } while (number != 0 && i < 10);

    while (i < width && i < 10) {
        str[i++] = ' ';
    }

    // 逆順を反転
    for (int j = 0; j < i / 2; j++) {
        char tmp = str[j];
        str[j] = str[i - j - 1];
        str[i - j - 1] = tmp;
    }

    str[i] = '\0'; // str[11]の範囲内なので安全(iは最大10)
    return str;
}

